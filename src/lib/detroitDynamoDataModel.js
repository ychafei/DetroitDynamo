const buildRolePermissions = (role, purpose, modulePermissions, sensitiveControls = []) => ({
  role,
  purpose,
  sensitiveControls,
  permissions: detroitDynamoAdminModules.map((module) => ({
    module,
    access: modulePermissions[module]?.access || 'none',
    scope: modulePermissions[module]?.scope || 'No assigned access until role policy is expanded.',
  })),
});

