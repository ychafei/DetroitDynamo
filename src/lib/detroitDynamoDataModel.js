const collectionIdByModel = Object.fromEntries(
  detroitDynamoCollectionPlan.map((item) => [item.model, item.collectionId]),
);

const collectionsForModels = (models) => models.map((model) => collectionIdByModel[model]).filter(Boolean);
const moduleSlug = (value) => String(value || '')
  .toLowerCase()
  .replace(/&/g, 'and')
  .replace(/[^a-z0-9]+/g, '-')
  .replace(/^-+|-+$/g, '');

const adminModule = ({
  module,
  purpose,
  primaryModels,
  ownerRoles,
  launchPhase,
  status,
  enabledActions,
  blockedUntil,
  sourceRoutes = [],
}) => ({
  module,
  slug: moduleSlug(module),
  purpose,
  primaryModels,
  collectionIds: collectionsForModels(primaryModels),
  ownerRoles,
  launchPhase,
  status,
  enabledActions,
  blockedUntil,
  sourceRoutes,
});

